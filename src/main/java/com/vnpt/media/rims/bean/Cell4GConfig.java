package com.vnpt.media.rims.bean;

/**
 *
 * @author Cyano
 */
public class Cell4GConfig {
    String vendor;
    String enodeb_name;
    String cell_name;
    String tac;
    String phyCellId;
    String lcrId;

    public String getVendor() {
        return vendor;
    }

    public void setVendor(String vendor) {
        this.vendor = vendor;
    }

    public String getEnodeb_name() {
        return enodeb_name;
    }

    public void setEnodeb_name(String enodeb_name) {
        this.enodeb_name = enodeb_name;
    }

    public String getCell_name() {
        return cell_name;
    }

    public void setCell_name(String cell_name) {
        this.cell_name = cell_name;
    }

    public String getTac() {
        return tac;
    }

    public void setTac(String tac) {
        this.tac = tac;
    }

    public String getPhyCellId() {
        return phyCellId;
    }

    public void setPhyCellId(String phyCellId) {
        this.phyCellId = phyCellId;
    }

    public String getLcrId() {
        return lcrId;
    }

    public void setLcrId(String lcrId) {
        this.lcrId = lcrId;
    }
    
    
}
